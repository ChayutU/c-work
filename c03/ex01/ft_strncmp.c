int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	while (n > 0)
	{
		if (*s1 < *s2)
			return (-1);
		else if (*s1 > *s2)
			return (1);
		if (*s1)
			s1++;
		if (*s2)
			s2++;
		n--;
	}
	return (0);
}
