#include <unistd.h>

void	ft_put_str(char *str)
{
	while (*str)
		write(1, str++, 1);
}

int	main(int argc, char *argv[])
{
	int	i;

	i = 1;
	while (i < argc)
	{
		ft_put_str(argv[i++]);
		ft_put_str("\n");
	}
	return (0);
}
